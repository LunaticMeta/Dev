    /**
     * A {@link FragmentPagerAdapter} that returns a fragment corresponding to
     * one of the sections/tabs/pages.
     */
    public class SectionsPagerAdapter extends FragmentPagerAdapter {
        Context mContext;
        public SectionsPagerAdapter(Context context, FragmentManager fm) {
            super(fm);
            mContext = context;
        }

        @Override
        public Fragment getItem(int position) {
            // getItem is called to instantiate the fragment for the given page.
            // Return a PlaceholderFragment (defined as a static inner class below).
            //return PlaceholderFragment.newInstance(position + 1);
            // 기본코드 주석처리 후 아래코드 추가f
            switch (position) {
                case 0:
                    return new activity_tab1();
                case 1:
                    return new activity_tab2();
                case 2:
                    return new activity_tab3();
                case 3:
                    return new activity_tab4();
            }
            return null;
        }

        @Override
        public int getCount() {return 4;}

        @Override
        public CharSequence getPageTitle(int position) {
            Locale I = Locale.getDefault();
            switch (position) {
                case 0:
                    return "수면 측정".toUpperCase(I);
                case 1:
                    return "수면 기록".toUpperCase(I);
                case 2:
                    return "수면 분석".toUpperCase(I);
                case 3:
                    return "기타".toUpperCase(I);
            }
            return null;
        }
    }

